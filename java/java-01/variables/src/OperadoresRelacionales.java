public class OperadoresRelacionales {
    public static void main(String[] args) {
        int i = 3;
        byte j = 7;    // compara el valode de la variable no compara el tipo de dato


        boolean b1 = i == j;  // pperador gual
        System.out.println("b1 = " + b1);

        boolean b2 = !b1;   // operador  not
        System.out.println("b2 = " + b2);
        
        boolean b3 = i >= j;    // operador mayo que >
        System.out.println("b3 = " + b3);










    }
}
